type HelpTab = 'howto' | 'features'

const tabs: { key: HelpTab; label: string }[] = [
  { key: 'howto', label: 'Как пользоваться' },
  { key: 'features', label: 'Особенности' },
]

export function HelpPage({ onLaunchOnboarding }: HelpPageProps) {
  const [activeTab, setActiveTab] = useState<HelpTab>('howto')
  const sections = activeTab === 'howto' ? howtoSections : featureSections

  return (
    <div className="help-page">
      <div className="card help-card">
        <div className="ui-panel-header">
          <div className="ui-panel-header__title">Справка</div>
          <div className="ui-panel-header__actions">
            <button
              className="ui-btn ui-btn--m"
              type="button"
              onClick={onLaunchOnboarding}
            >
              <svg
                width="16"
                height="16"
                viewBox="0 0 16 16"
                fill="none"
                aria-hidden="true"
                style={{ marginRight: 6 }}
              >
                <circle cx="8" cy="8" r="6.25" stroke="currentColor" strokeWidth="1.5" />
                <path
                  d="M6 6.5c0-1.1.9-2 2-2s2 .9 2 2c0 .7-.4 1.1-1 1.5-.6.4-1 .8-1 1.5"
                  stroke="currentColor"
                  strokeWidth="1.5"
                  strokeLinecap="round"
                />
                <circle cx="8" cy="11.5" r="0.75" fill="currentColor" />
              </svg>
              Пройти онбординг заново
            </button>
          </div>
        </div>

        <div className="help-tabs" role="tablist" aria-label="Разделы справки">
          <div className="ui-tabs ui-tabs--m">
            {tabs.map((tab) => (
              <button
                key={tab.key}
                type="button"
                role="tab"
                aria-selected={activeTab === tab.key}
                className={`ui-tab ui-tab--m${activeTab === tab.key ? ' is-active' : ''}`}
                onClick={() => setActiveTab(tab.key)}
              >
                {tab.label}
              </button>
            ))}
          </div>
        </div>

        <div className="help-body">
          {sections.map((section) => (
            <section className="help-section" key={section.key}>
              <h3 className="help-section__title">{section.title}</h3>
              {section.intro && (
                <p className="help-section__intro">{section.intro}</p>
              )}
              <ul className="help-list" role="list">
                {section.items.map((item) => (
                  <li className="help-item" key={item.title}>
                    <div className="help-item__title">{item.title}</div>
                    <div className="help-item__text">{item.text}</div>
                  </li>
                ))}
              </ul>
            </section>
          ))}
        </div>
      </div>
    </div>
  )
}
